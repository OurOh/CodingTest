package Bronze5;

import java.util.Scanner;

public class BOJ11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (sc.hasNext()) {
            String str = sc.nextLine();
            System.out.println(str);

        }
    }
}

// next는 단어 단위, nextLine은 한 줄 전체
